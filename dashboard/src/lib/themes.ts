export function applyTheme(themeKey: ThemeKey) {
  const theme = themes[themeKey];
  if (!theme) return;
  const root = document.documentElement;
  Object.entries(theme.colors).forEach(([prop, value]) => {
    root.style.setProperty(prop, value);
  });
  // Update color-scheme for light themes
  root.style.colorScheme = themeKey === 'cream' ? 'light' : 'dark';
  localStorage.setItem('ls-theme', themeKey);
}

export function getStoredTheme(): ThemeKey {
  if (typeof window === 'undefined') return 'midnight';
  return (localStorage.getItem('ls-theme') as ThemeKey) || 'midnight';
}
